#include <stdio.h>

int main()
{ // for storing a integer
    int a = 1;
    // for storing a decimal value
    float b;
    b = 1.34;
    // for storing any kind of character and it must be unde ' '
    char c = 'd';

    printf("the value of a is : %d \n", a);
    printf("the value of b is : %f \n", b);
    printf("the value of c is : %c \n", c);
    return 0;
}